package ex01;

public class Var04 {
    public static void main(String[] args) {
        // 문자열(무조건 첫글자 대문자)
        String s1 = "가나다";
        System.out.println(s1);

        // 연결 연산자 = +
        String name = "홍길동의 나이는";
        int age = 25;
        String s2 = "이다";
        System.out.println(name + " " + age + s2);
        // ""은 string으로 띄어쓰기의 역할

    }
}
